class PostRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Post::class);
    }

    /**
     * Method taking the 3 posts which have the most comments
     *
     * @return void
     */
    public function findTopPosts(){
        $limit = 3;
        return $this->createQueryBuilder('post')
                    // Adding HIDDEN after the alias declaration to abort the alias in the array
                    ->addSelect('COUNT(comments.id) AS HIDDEN comments_num')
                    ->innerJoin('post.comments', 'comments')
                    ->groupBy('post.id')
                    ->orderBy('comments_num', 'DESC')
                    ->setMaxResults($limit)
                    ->getQuery()
                    ->getResult();
    }

